package day6_java;

 public class TipeDataPecahan {
    public static void main(String[] args) {

        
        float a = 12.345f;
       
        // Menampilkan nilai float
        System.out.println(" suhu dini hari: " + a);
        
        double b = 12345.6789;
       
        // Menampilkan nilai double
        System.out.println(" luas bumi: " + b);
        
    }
}
   
